    @Override
    public void update(Observable o, Object arg) {
      this.valorActual = ((Coin) o).getValorActual();
      this.valorMaximo = ((Coin) o).getValorMaximo();
      this.valorMinimo = ((Coin) o).getValorMinimo();
      this.jTextField1.setText(String.valueOf(this.valorMaximo));
      this.jTextField2.setText(String.valueOf(this.valorMinimo));
      this.jTextField3.setText(String.valueOf(this.valorActual));
      
      datos.clear();
      datos.setValue(valorActual,"USD","Valor Actual");
      datos.setValue(valorMaximo, "USD","Valor Max");
      datos.setValue(valorMinimo,"USD", "Valor Min");
      
       
      JFreeChart graficoBarras = ChartFactory.createBarChart(
          "Precios",
          "Valores Precios",
          "USD",
          datos,
          PlotOrientation.VERTICAL,
          true,
          true,
          false
              
      );
      
      ChartPanel panel = new ChartPanel(graficoBarras);
      panel.setMouseWheelEnabled(true);
      panel.setPreferredSize(new Dimension(400,200));
      
      jPanel1.setLayout(new BorderLayout());
      jPanel1.add(panel,BorderLayout.NORTH);
      
      
      pack();
      repaint();
      
      
      this.setVisible(true);
    }
